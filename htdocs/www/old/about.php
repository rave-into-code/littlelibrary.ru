<strong><center>Что такое "Уличная библиотека"?</center></strong>
<br><br><br>
Уличная библиотека - это домик, в котором живут книги. Такие домики разной фигуры и разного фасона конструируют и устанавливают
в разных уголках земного шара для того, чтобы люди могли обмениваться книгами. Книги путешествуют из одной библиотечки
в другую, а читатели этих книг пишут комментарии о своих впечатлениях или просто замечательные истории из жизни - в их электронных
журналах, созданных на сайте <a href="http://www.bookcrossing.com/" target="_blank">Всемирной библиотеки буккроссинга</a>. Этот же сайт позволяет <a href="http://www.bookcrossing.com/hunt" target="_blank">отслеживать путешествия</a> этих книг
и уведомлять читателей о том, в каком домике есть какие книги.

